# Übung 2.1-1: Einfache if-Bedingung
#
# Schreibe ein Programm, das:
# 1. Den Benutzer nach einer Zahl fragt
# 2. Prüft, ob die Zahl größer als 10 ist
# 3. Wenn ja, gibt es "Die Zahl ist größer als 10" aus

# Lösung:
zahl = int(input("Gib eine Zahl ein: "))
if zahl > 10:
    print("Die Zahl ist größer als 10")

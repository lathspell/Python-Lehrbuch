# Schreibe ein Programm, das:
# 1. Die Zahl 7 mit 5 multipliziert und das Ergebnis ausgibt
# 2. Die Zahl 20 durch 4 teilt und das Ergebnis ausgibt
# 3. Die Zahl 15 um 7 erhöht und das Ergebnis ausgibt
# 4. Die Zahl 25 um 10 verringert und das Ergebnis ausgibt

# Lösung:
print("7 * 5 = " + str(7 * 5))
print("20 / 4 = " + str(20 / 4))
print("15 + 7 = " + str(15 + 7))
print("25 - 10 = " + str(25 - 10))
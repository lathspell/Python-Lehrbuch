# Übung 1.3-2: Mit Variablen rechnen
# 
# Schreibe ein Programm, das:
# 1. Den Benutzer nach zwei Zahlen fragt
# 2. Die Zahlen in Variablen speichert
# 3. Das Produkt der beiden Zahlen berechnet und ausgibt
# 4. Die Summe der beiden Zahlen berechnet und ausgibt

# Lösung:
zahl1 = int(input("Erste Zahl: "))
zahl2 = int(input("Zweite Zahl: "))
print("Das Produkt ist: " + str(zahl1 * zahl2))
print("Die Summe ist: " + str(zahl1 + zahl2))